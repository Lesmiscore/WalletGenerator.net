let a, b;
const kv = {};

for (let line of txt) {
  // range
  if (!a) {
    a = line;
    continue;
  }
  // char
  if (!b) {
    b = line;
    continue;
  }
  // address length (ignored)
  kv[a] = b;
  a = b = null;
}
console.log(JSON.stringify(kv));

const actualBitcoinB58Leading = new Array(256);
for (let key in kv) {
  let value = kv[key];
  if (value.length != 1) {
    // char range
    value = `[${value}]`;
  }
  if (key.indexOf("-") < 0) {
    // not a range
    actualBitcoinB58Leading[+key] = value;
  } else {
    // range
    let [start, end] = key.split("-");
    start = +start;
    end = +end;
    for (let num = start; num <= end; num++) {
      actualBitcoinB58Leading[num] = value;
    }
  }
}

